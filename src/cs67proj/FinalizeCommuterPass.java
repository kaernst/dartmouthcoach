    public FinalizeCommuterPass(Reservation r, Person p) {
        this.r = r;
        this.p = p;
        
        initComponents();
        
        this.isNewLondon = false;
        r.setIsOneWay(false);
        
        this.dept = r.getDept();
        if (dept.equals("NEW LONDON"))
            this.isNewLondon = true;
        From.setText("FROM: " + dept);
        
        this.dest = r.getDest();
        this.isLogan = false;
        if (dept.equals("BOSTON: LOGAN AIRPORT"))
            this.isLogan = true;
        To.setText("TO: " + dest);
        
        this.bags = r.getBags();
        this.adults = r.getAdults();
        this.kids = r.getKids();
        this.price = calcCost();
        this.paymentType = -1;
        
        Calendar cal = Calendar.getInstance();
        cal.add(Calendar.DAY_OF_YEAR, 60);
        this.expDate = cal.getTime();
        Format formatter = new SimpleDateFormat("MM/dd/yyyy");
        String exp = formatter.format(expDate);
        r.setExpDate(expDate);
        Expiration.setText("EXPIRES: " + exp);
    }

