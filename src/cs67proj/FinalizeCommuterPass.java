    public double calcCost() {
        double basePrice = 165.00;
        if (!isNewLondon && isLogan) {
            basePrice = 190.00;
        }
        else if (isNewLondon && !isLogan) {
            basePrice = 140.00;
        }
        else if (isNewLondon && isLogan) {
            basePrice = 165.00;
        }
        double totalPrice = adults * basePrice;
        
        price = totalPrice;
        Total.setText("TOTAL: $" + df.format(price));
        return price;
    }
