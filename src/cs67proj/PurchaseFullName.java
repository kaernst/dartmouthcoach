    private void LetterAction(String let) {
        if (let.length() == 0) {
            return;
        }
        if (field_selected == 0 && first_name.equals("First Name")) {
            first_name = let;
            FirstNameField.setText(first_name);
//            FirstNameField.validate();
        }
        else if (field_selected == 0) {
            first_name = first_name + let;
            FirstNameField.setText(first_name);
//            FirstNameField.validate();
        }
        else if (field_selected == 1 && middle_name.equals("Middle Initial")) {
            middle_name = let;
            MiddleNameField.setText(middle_name);
//            MiddleNameField.validate();
        }
        else if (field_selected == 1) {
            middle_name = middle_name + let;
            MiddleNameField.setText(middle_name);
//            MiddleNameField.validate();
        }
        else if (field_selected == 2 && last_name.equals("Last Name")) {
            last_name = let;
            LastNameField.setText(last_name);
//            LastNameField.validate();
        }
        else if (field_selected == 2) {
            last_name = last_name + let;
            LastNameField.setText(last_name);
//            LastNameField.validate();
        }
    }
