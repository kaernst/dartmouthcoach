    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        birth_date = jDateChooser1.getDate();
        if (first_name.equals("") || last_name.equals("") ||
                first_name.equals("First Name") || last_name.equals("Last Name")) {
            InvalidName.setVisible(true);
        }
        else if (birth_date == null) {
            EmptyDOB.setVisible(true);
        }
        else if (birth_date.equals(new Date()) || birth_date.after(new Date())) {
            InvalidDOB.setVisible(true);
        }
        else if (middle_name.equals("Middle Initial")) {
            middle_name = "";
        }
        else {
          
            Person p = CS67Proj.dir.findPerson(first_name, middle_name, last_name);
            if (p == null) {
                p = new Person(first_name, middle_name, last_name, birth_date);
                CS67Proj.dir.addPerson(p);
            }
            dispose();
            TicketInfo ticketform = new TicketInfo(p);
            ticketform.setVisible(true);
        }
    }//GEN-LAST:event_SubmitActionPerformed

