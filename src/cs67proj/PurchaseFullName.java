    private void BackspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BackspaceActionPerformed
        int len = 0;
        switch (field_selected) {
            case 0:
                len = first_name.length();
                if (len != 0) {
                    first_name = first_name.substring(0,len-1);
                    FirstNameField.setText(first_name);
                }   break;
            case 1:
                len = middle_name.length();
                if (len != 0) {
                    middle_name = middle_name.substring(0, len-1);
                    MiddleNameField.setText(middle_name);
                }   break;
            case 2:
                len = last_name.length();
                if (len != 0) {
                    last_name = last_name.substring(0, len-1);
                    LastNameField.setText(last_name);
                }   break;
            default:
                break;
        }
    }//GEN-LAST:event_BackspaceActionPerformed

